function checkpsw(str){
    if(str.length<=0){
        return pwdType_null;
    }else{
        var isnum = /^\d+$/.test(str);
        var islower = /^[a-z]+$/.test(str);
        var isupper = /^[A-Z]+$/.test(str);
        var isspec = /^\W/g.test(str);
        var includeNum = /[0-9]/g.test(str),
            includeLower = /[a-z]/g.test(str),
            includeUpper = /[A-Z]/g.test(str),
            includeSpec = /[\W]/g.test(str);
        if(isnum){
            return pwdType_number;
        }else if(islower){
            return pwdType_lower;
        }else if(isupper){
            return pwdType_upper;
        }else if(isspec){
            return pwdType_spec;
        }else{
            if(includeNum+includeLower+includeUpper+includeSpec == 4 || includeNum+includeLower+includeUpper+includeSpec == 0){
                return pwdType_mixAll;
            }else if(includeSpec){
                return pwdType_mixAll;
            }else{
                if(includeNum+includeLower+includeUpper == 3){
                    return pwdType_alphaNum;
                }else{
                    if(includeLower+includeUpper == 2){
                        return pwdType_alpha;
                    }else if(includeUpper+includeNum == 2){
                        return pwdType_upperNum;
                    }else if(includeLower+includeNum == 2){
                        return pwdType_lowerNum;
                    }
                }
                return pwdType_mixAll;
            }
        }
        
    }

}
